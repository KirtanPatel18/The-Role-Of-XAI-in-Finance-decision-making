def get_shap_vector_for_row(X_row_trans):
    """
    X_row_trans: array-like with shape (1, n_features_trans)
    returns: (shap_vec, base_value) where shap_vec shape = (n_features_trans,)
    """
    raw_shap = explainer.shap_values(X_row_trans)
    # Cases:
    # 1) raw_shap is list/tuple of arrays: [class0_array, class1_array] where each is (n_samples, n_features)
    # 2) raw_shap is array shape (n_samples, n_features) -> binary case flattened
    # 3) raw_shap may be (n_classes, n_samples, n_features) -> handle similarly
    if isinstance(raw_shap, (list,tuple)) and len(raw_shap) >= 2:
        # pick class 1
        arr = np.asarray(raw_shap[1])
        shap_vec = arr[0]  # first (and only) sample
        # base value: explainer.expected_value might be scalar or array per class
        base = explainer.expected_value[1] if hasattr(explainer, 'expected_value') and len(np.atleast_1d(explainer.expected_value))>1 else (explainer.expected_value if hasattr(explainer,'expected_value') else None)
        return shap_vec, base
    else:
        arr = np.asarray(raw_shap)
        if arr.ndim == 2:
            # shape (n_samples, n_features)
            return arr[0], (explainer.expected_value if hasattr(explainer,'expected_value') else None)
        if arr.ndim == 3:
            # maybe (n_classes, n_samples, n_features) -> pick class 1
            if arr.shape[0] >= 2:
                return arr[1,0,:], (explainer.expected_value[1] if hasattr(explainer,'expected_value') and len(np.atleast_1d(explainer.expected_value))>1 else None)
            else:
                return arr[0,0,:], (explainer.expected_value if hasattr(explainer,'expected_value') else None)
    # fallback – try to coerce
    raise ValueError("Unexpected shap value shape: " + str(np.asarray(raw_shap).shape))

# build feature names matching transform
def build_feature_names(ct, trans_mat):
    names = []
    for name, transformer, cols in ct.transformers_:
        if name == 'remainder':
            continue
        if name == 'num':
            names.extend(list(cols))
        elif name == 'cat':
            try:
                ohe = transformer.named_steps.get('ohe', None)
                if ohe is not None:
                    names.extend(list(ohe.get_feature_names_out(cols)))
                else:
                    names.extend(list(cols))
            except Exception:
                names.extend(list(cols))
        else:
            try:
                names.extend(list(cols))
            except Exception:
                pass
    if not names:
        # fallback to generic
        return [f"feat_{i}" for i in range(trans_mat.shape[1])]
    # ensure length match
    if len(names) != trans_mat.shape[1]:
        if len(names) > trans_mat.shape[1]:
            names = names[:trans_mat.shape[1]]
        else:
            need = trans_mat.shape[1] - len(names)
            names += [f"feat_extra_{i}" for i in range(need)]
    return names

