# Evaluate function (computes metrics, plots, and saves them)
def evaluate_and_save(pipe, X_eval, y_eval, tag):
    # predictions & probs
    y_pred = pipe.predict(X_eval)
    if hasattr(pipe, "predict_proba"):
        y_proba = pipe.predict_proba(X_eval)[:,1]
    else:
        # fallback to decision_function if available
        try:
            y_proba = pipe.decision_function(X_eval)
            # convert to 0-1 range with logistic (not perfect but OK for plotting)
            from scipy.special import expit
            y_proba = expit(y_proba)
        except Exception:
            y_proba = np.zeros(len(y_pred))

    # metrics
    report = classification_report(y_eval, y_pred, output_dict=True)
    cm = confusion_matrix(y_eval, y_pred)
    roc_auc = roc_auc_score(y_eval, y_proba) if len(np.unique(y_eval))>1 else None

    # save metrics JSON
    metrics_out = {
        'classification_report': report,
        'confusion_matrix': cm.tolist(),
        'roc_auc': float(roc_auc) if roc_auc is not None else None
    }
    with open(os.path.join(ARTIFACTS, f'metrics_lr_{tag}.json'), 'w') as f:
        json.dump(metrics_out, f, indent=2)
    print(f"Saved metrics_lr_{tag}.json")

    # save summary CSV (per-class precision/recall/f1)
    rows = []
    for cls, vals in report.items():
        if cls in ['accuracy','macro avg','weighted avg']:
            continue
        row = {'class': cls}
        row.update(vals)
        rows.append(row)
    import pandas as pd
    pd.DataFrame(rows).to_csv(os.path.join(ARTIFACTS, f'model_performance_summary_lr_{tag}.csv'), index=False)
    print(f"Saved model_performance_summary_lr_{tag}.csv")

    # ROC plot
    if roc_auc is not None:
        fpr, tpr, _ = roc_curve(y_eval, y_proba)
        plt.figure(figsize=(6,5))
        plt.plot(fpr, tpr, label=f'ROC (AUC={roc_auc:.3f})')
        plt.plot([0,1],[0,1],'--', color='gray')
        plt.xlabel('False Positive Rate'); plt.ylabel('True Positive Rate')
        plt.title(f'ROC Curve — LR ({tag})')
        plt.legend(loc='lower right')
        plt.grid(True)
        plt.savefig(os.path.join(VIZ, f'roc_lr_{tag}.png'), bbox_inches='tight')
        plt.close()
        print(f"Saved viz/roc_lr_{tag}.png")

    # Precision-Recall plot
    prec, rec, _ = precision_recall_curve(y_eval, y_proba)
    ap = np.trapz(prec, rec)  # approximate area under PR (not average_precision_score but fine for plot label)
    plt.figure(figsize=(6,5))
    plt.plot(rec, prec, label=f'PR (area={ap:.3f})')
    plt.xlabel('Recall'); plt.ylabel('Precision')
    plt.title(f'Precision-Recall — LR ({tag})')
    plt.grid(True)
    plt.savefig(os.path.join(VIZ, f'pr_lr_{tag}.png'), bbox_inches='tight')
    plt.close()
    print(f"Saved viz/pr_lr_{tag}.png")

    # Confusion matrix (absolute and normalized)
    disp = ConfusionMatrixDisplay(confusion_matrix=cm, display_labels=['No Default','Default'])
    fig, ax = plt.subplots(figsize=(5,4))
    disp.plot(ax=ax, cmap='Blues', values_format='d')
    ax.set_title(f'Confusion matrix (LR) — {tag}')
    fig.savefig(os.path.join(VIZ, f'confusion_matrix_lr_{tag}.png'), bbox_inches='tight')
    plt.close(fig)
    print(f"Saved viz/confusion_matrix_lr_{tag}.png")

    # Return basic numbers for quick inspection
    return {'roc_auc': roc_auc, 'precision': precision_score(y_eval, y_pred, zero_division=0),
            'recall': recall_score(y_eval, y_pred, zero_division=0), 'f1': f1_score(y_eval, y_pred, zero_division=0)}

