def safe_get_feature_names_from_ct(ct, numeric_cols, cat_cols):
    names = []
    # numeric part
    if numeric_cols:
        names.extend(numeric_cols)
    # categorical part
    if cat_cols:
        try:
            # try to retrieve OHE from fitted pipeline
            for name, transformer, cols in ct.transformers_:
                if name == 'cat':
                    # transformer is a Pipeline; get 'ohe' step
                    if hasattr(transformer, 'named_steps') and 'ohe' in transformer.named_steps:
                        ohe = transformer.named_steps['ohe']
                        try:
                            ohe_names = list(ohe.get_feature_names_out(cols))
                        except NotFittedError:
                            # fallback: create safe names like col_val or col_<i>
                            ohe_names = []
                            for col in cols:
                                ohe_names.append(col)
                        names.extend(ohe_names)
                    else:
                        # no named_steps-> fallback
                        names.extend(cols)
                    break
        except Exception:
            # any unexpected issue -> fallback to raw cat names
            names.extend(cat_cols)
    return names

