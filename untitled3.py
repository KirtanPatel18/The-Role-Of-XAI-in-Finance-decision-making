    sha256 = h.hexdigest()
    with open(readme_path, 'w') as f:
        f.write(f"# Data README\n\nCanonical CSV: `{canonical}`\n\nPath: `{canonical_path}`\n\nSHA256: `{sha256}`\n\nSource: UCI Credit Card dataset (record in project notes)\n")
