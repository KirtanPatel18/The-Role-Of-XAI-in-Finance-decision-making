def safe_extract_lime_list(exp, pred_label):
    """
    Return a list of (feature_str, weight_float) robustly from a lime explanation object `exp`.
    Tries exp.as_list(label), exp.as_map(), and sensible fallbacks.
    """
    # 1) try as_list with label
    try:
        maybe = exp.as_list(label=pred_label)
        # sometimes as_list returns list of tuples -> good
        if isinstance(maybe, list) and all(isinstance(t, tuple) and len(t)==2 for t in maybe):
            return maybe
        # sometimes as_list can return a scalar or other shape -> ignore
    except Exception:
        pass

    # 2) try as_map (gives dict: label -> list of (idx, weight))
    try:
        amap = exp.as_map()
        if isinstance(amap, dict) and pred_label in amap:
            pairs = []
            for idx, weight in amap[pred_label]:
                fname = feature_names[idx] if idx < len(feature_names) else f"feat_{idx}"
                pairs.append((fname, float(weight)))
            if pairs:
                return pairs
    except Exception:
        pass

    # 3) try exp.as_list() without label (sometimes returns list of (label, list) or list of tuples)
    try:
        maybe2 = exp.as_list()
        # if format is [(feat, weight), ...] -> accept
        if isinstance(maybe2, list) and all(isinstance(t, tuple) and len(t)==2 for t in maybe2):
            return maybe2
        # if format is [(label, [(feat, weight), ...]), ...] -> find predicted label entry
        if isinstance(maybe2, list) and len(maybe2)>0 and isinstance(maybe2[0], tuple) and isinstance(maybe2[0][1], list):
            # find entry for pred_label if present
            for item in maybe2:
                lbl, lst = item
                if lbl == pred_label and isinstance(lst, list):
                    return lst
    except Exception:
        pass

    # 4) As last resort, try exp.as_map() keys and flatten first available
    try:
        amap2 = exp.as_map()
        for lbl, arr in amap2.items():
            pairs = []
            for idx, weight in arr:
                fname = feature_names[idx] if idx < len(feature_names) else f"feat_{idx}"
                pairs.append((fname, float(weight)))
            if pairs:
                return pairs
    except Exception:
        pass

    # Final fallback: return top-10 zeros with feature names
    return [(fn, 0.0) for fn in feature_names[:10]]

